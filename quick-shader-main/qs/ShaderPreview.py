# User-Accessible Method:
# preview_shader() – Launches the shader preview window.

# Internal Methods:
# _initialize_window() – Sets up the preview window and rendering context.

# _load_shader(shader_path) – Loads and compiles the shader from a file.

# _apply_shader() – Binds the shader to the rendering pipeline.

# _create_preview_mesh() – Generates a simple mesh (quad/sphere) for previewing the shader.

# _handle_user_input() – Manages input for rotating, zooming, and interacting with the preview.

# _render_frame() – Renders each frame with the applied shader.

# _update_shader_parameters() – Updates shader variables dynamically.

# _toggle_wireframe_mode() – Enables/disables wireframe mode for debugging.

# _shutdown_preview() – Cleans up resources when the preview window is closed.