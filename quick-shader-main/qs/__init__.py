#  Final Class Structure for Quick Shader Library
# ImageAnalyzer → Extracts shader-related data from an image.
# ShaderModifier → Modifies shader attributes (brightness, contrast, light direction, etc.).
# ShaderGenerator → Generates GLSL or HLSL shader files based on extracted & modified data.
# ShaderPreview → Renders a preview of the shader for visualization.
# ShaderUtils → Handles file operations, format conversions, and shader optimizations.